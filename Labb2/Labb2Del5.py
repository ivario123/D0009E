from math import *
#///////////////////////////////////////////////////////////////////////////////////////
#---------------------------------------Deriverare-------------------------------------
#\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\
derivative =lambda f,x,h: 1.0 / (2 * h) * (f(x + h) - f(x - h))
#///////////////////////////////////////////////////////////////////////////////////////
#-----------------------------------Ekvationsl√∂saren-----------------------------------
#\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\
def solve(f,x0,h):
    xn = 0
    while True:
        xn = x0 - (f(x0) / derivative(f, x0, h))
        if abs(xn - x0) < h:
            return xn
        x0 = xn
#///////////////////////////////////////////////////////////////////////////////////////
#--------------------------------Testar funktionerna-----------------------------------
#\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\\
f1 = lambda x:x**2-1
f2 = lambda x:2**x-1
print(solve(f1,-0.001,0.001))
print(solve(f2,1,0.001))